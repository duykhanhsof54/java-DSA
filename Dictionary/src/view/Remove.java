     private void removeFile(Word rem) throws FileNotFoundException, IOException{
       
        File tempFile = new File("temp.txt"); // đây là file temp, trước đó không được tạo sẵn
        FileWriter fw;
        BufferedWriter bw = null; // cái này để ghi vào file temp
        Scanner sc = new Scanner(new File("data1.txt")); // đọc dữ liệu file oldFile
        try {
            fw = new FileWriter(tempFile,true); // try catch này khởi tạo thôi
            bw = new BufferedWriter(fw);
        } catch (IOException e) {
            e.printStackTrace();
        }
        while (sc.hasNextLine()) {
            String str = sc.nextLine();
            if(!str.matches(rem.getWord_target()+".*"))
                try {
                    bw.write(str + "\n");
                } catch (IOException e) {
                    e.printStackTrace();
                }
        }
        try {
            bw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        sc.close();
        try {
             File oldFile = new File("data1.txt");
            if (oldFile.delete()) {
                JOptionPane.showMessageDialog(rootPane,"oce!");
            } else {
                JOptionPane.showMessageDialog(rootPane,"sịt!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
         File newFile = new File("data1.txt");
        tempFile.renameTo(newFile);
    
        
     
    }
