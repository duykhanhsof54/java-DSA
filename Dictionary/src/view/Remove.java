    private void ButtonAcpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAcpActionPerformed
         String word_remove = texeRemove.getText();
          boolean check = true;
           if (word_remove.length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Word không được để trống!");
            check = false;
        } 
       
        if (word_remove.matches("[^a-zA-Z]+")){
             JOptionPane.showMessageDialog(rootPane,"Word lỗi định dạng!");
            check = false;
        } 
        if (check) {
             Word rem = new Word(null,null);
            for (Word word : home.getListWord()) {
                if(word.getWord_target().equals(word_remove)) {
                    rem = word;
                    break;}
            }
             try {
                 removeFile(rem);
                  JOptionPane.showMessageDialog(rootPane,"oce rồi baeee ơi !");
             } catch (IOException ex) {
                 Logger.getLogger(Remove.class.getName()).log(Level.SEVERE, null, ex);
             }
        }
    }//GEN-LAST:event_ButtonAcpActionPerformed

